/*

Math.round()
        The Math.round() function returns the value of a number rounded to the nearest integer.

    Syntax  :=
                Math.round(x)

    Example :=
                console.log(Math.round(0.9));       // 1
                console.log(Math.round(5.95)       //  6
                console.log(Math.round(-5.05)      // -5


*/